package com.qfedu.babytree.pojo;

public class Sign {
    private Integer sigId;

    private Integer sigUserid;

    private Integer sigTimes;

    private Integer sigTime;

    public Integer getSigId() {
        return sigId;
    }

    public void setSigId(Integer sigId) {
        this.sigId = sigId;
    }

    public Integer getSigUserid() {
        return sigUserid;
    }

    public void setSigUserid(Integer sigUserid) {
        this.sigUserid = sigUserid;
    }

    public Integer getSigTimes() {
        return sigTimes;
    }

    public void setSigTimes(Integer sigTimes) {
        this.sigTimes = sigTimes;
    }

    public Integer getSigTime() {
        return sigTime;
    }

    public void setSigTime(Integer sigTime) {
        this.sigTime = sigTime;
    }
}